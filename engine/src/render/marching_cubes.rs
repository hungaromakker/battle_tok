/// Marching Cubes mesh generator
pub struct MarchingCubes {
    /// Resolution (number of cells per axis)
    resolution: u32,
    /// Smoothness factor for smooth union SDF (optional)
    smoothness: f32,
}

impl MarchingCubes {
    /// Create a new Marching Cubes generator
    ///
    /// # Arguments
    /// * `resolution` - Number of cells per axis (e.g., 64 = 64x64x64 grid)
    pub fn new(resolution: u32) -> Self {
        Self {
            resolution: resolution.max(2),
            smoothness: 0.1,
        }
    }
    
    /// Set smoothness factor for vertex interpolation
    pub fn with_smoothness(mut self, smoothness: f32) -> Self {
        self.smoothness = smoothness;
        self
    }
    
    /// Generate mesh from an SDF function within given bounds
    ///
    /// # Arguments
    /// * `sdf` - Function that returns signed distance at a point
    /// * `min` - Minimum bounds
    /// * `max` - Maximum bounds
    /// * `color` - Color for the generated mesh
    ///
    /// # Returns
    /// Tuple of (vertices, indices) for the generated mesh
    pub fn generate_mesh<F>(
        &self,
        sdf: F,
        min: Vec3,
        max: Vec3,
        color: [f32; 4],
    ) -> (Vec<BlockVertex>, Vec<u32>)
    where
        F: Fn(Vec3) -> f32,
    {
        let mut vertices = Vec::new();
        let mut indices = Vec::new();
        
        // Cell size
        let size = max - min;
        let cell_size = size / self.resolution as f32;
        
        // Sample SDF at all corners
        let sample_count = (self.resolution + 1) as usize;
        let mut samples = vec![0.0f32; sample_count * sample_count * sample_count];
        
        for z in 0..=self.resolution {
            for y in 0..=self.resolution {
                for x in 0..=self.resolution {
                    let pos = min + Vec3::new(x as f32, y as f32, z as f32) * cell_size;
                    let idx = (z as usize * sample_count + y as usize) * sample_count + x as usize;
                    samples[idx] = sdf(pos);
                }
            }
        }
        
        // Vertex cache for edge interpolation (to avoid duplicates)
        let mut edge_cache: HashMap<(u32, u32, u32, u8), u32> = HashMap::new();
        
        // Process each cell
        for z in 0..self.resolution {
            for y in 0..self.resolution {
                for x in 0..self.resolution {
                    self.process_cell(
                        x, y, z,
                        min, cell_size,
                        &samples,
                        sample_count,
                        color,
                        &mut vertices,
                        &mut indices,
                        &mut edge_cache,
                    );
                }
            }
        }
        
        // Calculate normals from SDF gradient
        for vertex in &mut vertices {
            let pos = Vec3::from_array(vertex.position);
            let eps = cell_size.min_element() * 0.1;
            
            let normal = Vec3::new(
                sdf(pos + Vec3::X * eps) - sdf(pos - Vec3::X * eps),
                sdf(pos + Vec3::Y * eps) - sdf(pos - Vec3::Y * eps),
                sdf(pos + Vec3::Z * eps) - sdf(pos - Vec3::Z * eps),
            ).normalize_or_zero();
            
            vertex.normal = normal.to_array();
        }
        
        (vertices, indices)
    }
    
    /// Process a single cell
    fn process_cell(
        &self,
        x: u32, y: u32, z: u32,
        min: Vec3,
        cell_size: Vec3,
        samples: &[f32],
        sample_count: usize,
        color: [f32; 4],
        vertices: &mut Vec<BlockVertex>,
        indices: &mut Vec<u32>,
        edge_cache: &mut HashMap<(u32, u32, u32, u8), u32>,
    ) {
        // Get corner values
        let mut corner_values = [0.0f32; 8];
        for (i, offset) in CORNER_OFFSETS.iter().enumerate() {
            let cx = x + offset[0] as u32;
            let cy = y + offset[1] as u32;
            let cz = z + offset[2] as u32;
            let idx = (cz as usize * sample_count + cy as usize) * sample_count + cx as usize;
            corner_values[i] = samples[idx];
        }
        
        // Determine cube configuration index (8 bits, one per corner)
        let mut cube_index = 0u8;
        for i in 0..8 {
            if corner_values[i] < 0.0 {
                cube_index |= 1 << i;
            }
        }
        
        // Skip if completely inside or outside
        if cube_index == 0 || cube_index == 255 {
            return;
        }
        
        // Get edge flags
        let edge_flags = EDGE_TABLE[cube_index as usize];
        if edge_flags == 0 {
            return;
        }
        
        // Calculate vertex position for each active edge
        let mut edge_vertices = [0u32; 12];
        for edge in 0..12 {
            if (edge_flags & (1 << edge)) != 0 {
                // Check cache first
                let cache_key = (x, y, z, edge as u8);
                if let Some(&cached_idx) = edge_cache.get(&cache_key) {
                    edge_vertices[edge] = cached_idx;
                } else {
                    // Interpolate vertex position along edge
                    let (v0, v1) = EDGE_VERTICES[edge];
                    let val0 = corner_values[v0];
                    let val1 = corner_values[v1];
                    
                    // Linear interpolation: find where SDF crosses zero
                    let t = if (val1 - val0).abs() > 1e-6 {
                        (-val0 / (val1 - val0)).clamp(0.0, 1.0)
                    } else {
                        0.5
                    };
                    
                    let p0 = Vec3::new(
                        x as f32 + CORNER_OFFSETS[v0][0],
                        y as f32 + CORNER_OFFSETS[v0][1],
                        z as f32 + CORNER_OFFSETS[v0][2],
                    );
                    let p1 = Vec3::new(
                        x as f32 + CORNER_OFFSETS[v1][0],
                        y as f32 + CORNER_OFFSETS[v1][1],
                        z as f32 + CORNER_OFFSETS[v1][2],
                    );
                    
                    let local_pos = p0 + (p1 - p0) * t;
                    let world_pos = min + local_pos * cell_size;
                    
                    let vertex_idx = vertices.len() as u32;
                    vertices.push(BlockVertex::new(
                        world_pos,
                        Vec3::ZERO, // Normal will be calculated later
                        color,
                    ));
                    
                    edge_cache.insert(cache_key, vertex_idx);
                    edge_vertices[edge] = vertex_idx;
                }
            }
        }
        
        // Generate triangles from table
        let tri_entry = &TRI_TABLE[cube_index as usize];
        let mut i = 0;
        while i < 16 && tri_entry[i] != -1 {
            indices.push(edge_vertices[tri_entry[i] as usize]);
            indices.push(edge_vertices[tri_entry[i + 1] as usize]);
            indices.push(edge_vertices[tri_entry[i + 2] as usize]);
            i += 3;
        }
    }
}

/// Generate mesh from a group of merged building blocks
///
/// This is the main entry point for SDF-to-mesh conversion.
pub fn generate_merged_mesh(
    blocks: &[super::building_blocks::BuildingBlock],
    smoothness: f32,
    resolution: u32,
    color: [f32; 4],
) -> (Vec<BlockVertex>, Vec<u32>) {
    if blocks.is_empty() {
        return (Vec::new(), Vec::new());
    }
    
    // Calculate bounds
    let mut min = Vec3::splat(f32::MAX);
    let mut max = Vec3::splat(f32::MIN);
    
    for block in blocks {
        let aabb = block.aabb();
        min = min.min(aabb.min);
        max = max.max(aabb.max);
    }
    
    // Add small margin
    let margin = (max - min).max_element() * 0.1;
    min -= Vec3::splat(margin);
    max += Vec3::splat(margin);
    
    // Create combined SDF function
    let sdf = |p: Vec3| -> f32 {
        let mut result = f32::MAX;
        let mut first = true;
        
        for block in blocks {
            let d = block.sdf(p);
            if first {
                result = d;
                first = false;
            } else {
                // Smooth union
                let h = (0.5 + 0.5 * (d - result) / smoothness).clamp(0.0, 1.0);
                result = d + (result - d) * h - smoothness * h * (1.0 - h);
            }
        }
        
        result
    };
    
    // Generate mesh
    let mc = MarchingCubes::new(resolution).with_smoothness(smoothness);
    mc.generate_mesh(sdf, min, max, color)
}

